do $$ begin create policy "members read bunny board tasks" on public.bunny_board_tasks for select to authenticated using (true); exception when duplicate_object then null; end $$;
do $$ begin create policy "leadership manage bunny board tasks" on public.bunny_board_tasks for all to authenticated using (exists(select 1 from public.profiles p where p.id=auth.uid() and p.role in ('owner','admin','assistant_leader'))) with check (exists(select 1 from public.profiles p where p.id=auth.uid() and p.role in ('owner','admin','assistant_leader'))); exception when duplicate_object then null; end $$;
do $$ begin create policy "members read bunny statuses" on public.bunny_task_status for select to authenticated using (true); exception when duplicate_object then null; end $$;
do $$ begin create policy "members manage own bunny status" on public.bunny_task_status for all to authenticated using (user_id=auth.uid()) with check (user_id=auth.uid()); exception when duplicate_object then null; end $$;

