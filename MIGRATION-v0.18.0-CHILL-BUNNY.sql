create table if not exists public.bunny_task_library (
  id bigint generated by default as identity primary key,
  category text not null,
  name text not null,
  amount integer not null default 1,
  icon text not null default '🐰',
  description text,
  active boolean not null default true,
  created_at timestamptz not null default now()
);
create table if not exists public.bunny_board (
  id bigint generated by default as identity primary key,
  board_date date not null default current_date,
  published_at timestamptz not null default now(),
  published_by uuid references public.profiles(id),
  active boolean not null default true
);
create table if not exists public.bunny_board_tasks (
  board_id bigint references public.bunny_board(id) on delete cascade,
  task_id bigint references public.bunny_task_library(id) on delete cascade,
  primary key(board_id,task_id)
);
create table if not exists public.bunny_task_status (
  board_id bigint references public.bunny_board(id) on delete cascade,
  task_id bigint references public.bunny_task_library(id) on delete cascade,
  user_id uuid references public.profiles(id) on delete cascade,
  status text not null check(status in ('ready','preparing','skip')),
  updated_at timestamptz not null default now(),
  primary key(board_id,task_id,user_id)
);
alter table public.bunny_task_library enable row level security;
alter table public.bunny_board enable row level security;
alter table public.bunny_board_tasks enable row level security;
alter table public.bunny_task_status enable row level security;

